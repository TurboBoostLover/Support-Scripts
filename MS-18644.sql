Declare @clientId int =1, -- SELECT Id, Title FROM Client 
	@Entitytypeid int =1; -- SELECT * FROM EntityType (1 = course, 2 = program, 6 = module)

declare @templateId integers

INSERT INTO @templateId
SELECT mt.MetaTemplateId
FROM MetaTemplateType mtt
    INNER JOIN MetaTemplate mt ON mtt.MetaTemplateTypeId = mt.MetaTemplateTypeId
WHERE mtt.EntityTypeId = @entityTypeId
		AND mtt.MetaTemplateTypeId in (41)		--comment back in if just doing some of the mtt's

declare @FieldCriteria table (
	TabName nvarchar(255) index ixRecalcFieldCriteria_TabName,
	TableName sysname index ixRecalcFieldCriteria_TableName,
	ColumnName sysname index ixRecalcFieldCriteria_ColumnName,
	Action nvarchar(max)
);
/************************* Put fields Here ***********************
*************************Only Edit Values************************/
insert into @FieldCriteria (TabName, TableName, ColumnName,Action)
values
('Entrance Skills', 'CourseContentReviewRequisiteCourseObjective', 'CourseObjectiveId', 'check2')

declare @Fields table (
	FieldId int primary key,
	SectionId int,
	Action nvarchar(max),
	TabId int,
	TemplateId int,
	sortorder int,
	mtt int
);

insert into @Fields (FieldId,SectionId,Action,TabId,TemplateId,sortorder, mtt)
select msf.metaselectedfieldid,msf.MetaSelectedSectionId,rfc.Action,mss.MetaSelectedSectionId,mss.MetaTemplateId ,msf.RowPosition, mt.MetaTemplateTypeId
from MetaTemplate mt
inner join MetaSelectedSection mss
	on mt.MetaTemplateId = mss.MetaTemplateId
inner join MetaSelectedSection mss2
	on mss.MetaSelectedSectionId = mss2.MetaSelectedSection_MetaSelectedSectionId
inner join MetaSelectedField msf
	on mss2.MetaSelectedSectionId = msf.MetaSelectedSectionId
inner join MetaAvailableField maf
	on msf.MetaAvailableFieldId = maf.MetaAvailableFieldId
inner join @FieldCriteria rfc
	on ( maf.TableName = rfc.TableName and maf.ColumnName = rfc.ColumnName)		--uncomment tab name if tempalate have different tab name (likely in reports)
where mt.MetaTemplateId  in (select * from @templateId)

/********************** Changes go HERE **************************************************/
INSERT INTO MetaSelectedSectionAttribute
(Name, Value, MetaSelectedSectionId)
SELECT 'FilterSourceTable', 'CourseContentReview', SectionId FROM @Fields
UNION
SELECT 'FilterSourceColumn', 'CourseRequisiteId', SectionId FROM @Fields
UNION
SELECT 'FilterFieldTable', 'CourseContentReviewRequisiteCourseObjective', SectionId FROM @Fields
UNION
SELECT 'FilterFieldColumn', 'CourseObjectiveId', SectionId FROM @Fields
UNION
SELECT 'FilterColumnName', 'NotUsed', SectionId FROM @Fields

UPDATE MetaForeignKeyCriteriaClient
SET CustomSql = 'Select 
    co.Id as Value
    ,Coalesce(co.[Text],co.GroupHeading) As Text
from CourseObjective co 
    inner join Course c on co.CourseId = c.Id 
where c.ClientId = @clientId
UNION
select 
    co.Id as Value
    ,''<label style = "color: red;">'' + co.Text +''</label>'' as Text
from CourseContentReviewRequisiteCourseObjective cro
    inner join CourseContentReview ccr on ccr.id = cro.CourseContentReviewId
    inner join CourseRequisite cr on cr.id = ccr.CourseRequisiteId
    inner join CourseObjective co on co.Id = cro.CourseObjectiveId
where ccr.CourseId = @entityId
and co.CourseId <> cr.Requisite_CourseId'
WHERE Id = 114
/****************************** update templates ******************************************/
update MetaTemplate
set LastUpdatedDate = getdate()
where MetaTemplateId in (select Distinct templateId FROM @Fields)

